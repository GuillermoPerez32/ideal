class House {
  final String name;
  final String state;
  final String city;
  final int price;
  final String image;
  final String between;
  final int bathrooms;
  final int bedrooms;
  final int meters;

  House({
    required this.state,
    required this.city,
    required this.price,
    required this.image,
    required this.name,
    required this.between,
    required this.bathrooms,
    required this.bedrooms,
    required this.meters,
  });
}
